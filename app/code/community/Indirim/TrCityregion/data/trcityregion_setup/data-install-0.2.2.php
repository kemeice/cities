// Convert Turkish characters to English if necessary  
function turkce_cevir($in) {

	$trans=array(
					"Ç" => "C",
					"Ğ" => "G",
					"İ" => "I",
					"Ö" => "O",
					"Ş" => "S",
					"Ü" => "U",
					"ç" => "c",
					"ğ" => "g",
					"ı" => "i",
					"ö" => "o",
					"ş" => "s",
					"ü" => "u",
				);
	
	return strtr($in,$trans);

} // function sonu #######################################################################################
